/* ************** SWITCH ******************
Basic Syntax 

switch (key) {
    case value:
        
        break;

    default:
        break;
}
*/

const day = "Saturday"

switch (day) {
    case "Monday":
        console.log("Monday");
        break;
    case "Tuesday":
        console.log("Tuesday");
        break;
    case "wednesday":
        console.log("wednesday");
        break;
    case "Thursday":
        console.log("Thursday");
        break;
    case "Friday":
        console.log("Friday");
        break;
    case "Saturday":
        console.log("Saturday");
        break;
    case "Sunday":
        console.log("Sunday");
        break;
    default:
        break;
}